package com.mycompany.bucles;

/**Imprimir por pantalla los numeros del 1 al 10 con el Bucle Do While**
 *
 * @author User
 */
public class EjemploDoWhile {

  public static void main(String[] args) {
    int i = 1; 
    do {      
      System.out.println(i);
      i++;
    } while (i < 11);
  }
  
}
